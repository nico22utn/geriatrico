        public void llenarTabla(){
        modelo.setRowCount(0);
        List<DTOEspecializacion> listaespecialidad = controlador.buscar();
        for(DTOEspecializacion especialidad : listaespecialidad){
            modelo.addRow(new Object[]{especialidad.getIdEspecializacion(),especialidad.getNombreEspecializacion()});
            
        }
    }

