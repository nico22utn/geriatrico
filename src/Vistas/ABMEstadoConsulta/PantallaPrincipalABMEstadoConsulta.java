    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int resp = JOptionPane.showConfirmDialog(null, "Â¿Desea dar de baja este estado?", "Alerta!", 0);
        if(resp == 0){
            if(controlador.iniciarBaja(Long.parseLong(modelo.getValueAt(jTable1.getSelectedRow(),0).toString()))){
                JOptionPane.showMessageDialog(null, "El estado se dio de baja exitosamente", "Exito", 1);
            }else{

                JOptionPane.showMessageDialog(null, "Existe una Consulta con ese estado asociado, no se puede dar de baja, primero debe dar de baja a la Consulta", "Error", 0);
            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

