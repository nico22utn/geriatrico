    public void llenarTabla(){
        modelo.setRowCount(0);
        List<DTORol> listaRol = controlador.buscar();
        for(DTORol rol : listaRol){
            modelo.addRow(new Object[]{rol.getIdRol(),rol.getNombreRol()});
            
        }
    }

